# コメントアウト (Java の //)
'''
複数行のコメントアウト (Javaの /* */)
'''

# 標準出力への出力
print("Hello World")  # System.out.println("Hello World");
print('Hello World')  # 文字列はシングルクォーテーション（''）も使える
print(1)

# 複数文字列の出力
print("Hello", "World")

# 数字の出力
print(1 + 2)
print(1, 2)

# 改行せずに出力
print("Hello World", end="")  # System.out.print("Hello World");

print("Hello World")

# 改行コード
print("Hello\nWorld")  # \n を出力すると改行できる
